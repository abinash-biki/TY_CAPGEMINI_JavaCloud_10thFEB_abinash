package com.org.acpgiminiloops;

public class LoopEx2 {
	public static void main(String[] args) {
		for (int i = 1; i<=10 ; i++) {
			
			System.out.println(i*12);
		}
		System.out.println("**********************");
		int j=0;
		for (;j<10 ;j++) {
			System.out.println((j+1)*13);
		}
		System.out.println("*****************8");
		int k=0;
		for (;k<10 ;k++) {
			System.out.println((k+1)*13);
		}
			
		}
	}


