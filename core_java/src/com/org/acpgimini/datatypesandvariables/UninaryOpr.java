package com.org.acpgimini.datatypesandvariables;

public class UninaryOpr {
public static void main(String[] args) {
	int i=10;
	int k =10;
	boolean isTrue=true;
	int j=-i;
	System.out.println(j);
	System.out.println(-i);
	System.out.println(!isTrue);
	System.out.println("value of i in pre increment :"+ ++i);//pre increment:-the value will be incremented irst then the imcremented value will be implemented
	System.out.println(i);
	System.out.println("value of j in post increment :" + k++);//post incriment:-the value will be implemented first then it will increment
	System.out.println(k);
	System.out.println("value of i with pre drcrement :" + --i);//pre-decrement
	System.out.println(i);
	System.out.println("value of k in post Decrement :" + k++);//post decremennt     
}
}        
 