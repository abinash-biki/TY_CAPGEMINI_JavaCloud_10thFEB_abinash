package com.org.acpgimini.methods;

public class AreaCalculator {
	public int calculateAreaOfSquire(int side ) {
		return side * side;
	}
public int calculateAreaOfRectangle(int length,int width) {
	return length * width;
}
public double calculateAreaOfCircle(int r) {
	return 3.14*r*r;
}
}