package com.org.acpgimini.controlflow;

public class SwitchCase2 {
	public static void main(String[] args) {
		int day=3;
	switch (day)
	{
	case 1:
		System.out.println("sun");
          break;
	case 2:
		System.out.println("mon");
          break;
     case 3:
		System.out.println("tue");
        break;

	default:
		System.out.println("invalid");
		break;
	}

	}}
